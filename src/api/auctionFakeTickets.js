function fetchAuctionTickets(query) {
  let mainData;
  if (query) {
    mainData = data[query.asset];
  }
  if (query.currentPage === 1) {
    return new Promise((reslove) => setTimeout(reslove, 1000))
      .then(() => ({
        ticketsData: mainData.slice(0, query.number),
        totalPages: Math.floor(mainData.length / 10),
      }));
  }
  return new Promise((reslove) => setTimeout(reslove, 1000))
    .then(() => ({
      ticketsData: mainData.slice(query.currentPage * 10,
        query.currentPage * 10 + query.number),
      totalPages: Math.floor(mainData.length / 10),
    }));
}

