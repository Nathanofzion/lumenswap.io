function fetchAuctionBids(query) {
  if (!query) {
    return new Promise((reslove) => setTimeout(reslove, 1000))
      .then(() => ({
        bidsData: bids,
      }));
  }
  if (query.currentPage === 1) {
    return new Promise((reslove) => setTimeout(reslove, 1000))
      .then(() => ({
        bidsData: bids.slice(0, query.number),
        totalPages: Math.floor(bids.length / 10),
      }));
  }
  return new Promise((reslove) => setTimeout(reslove, 1000))
    .then(() => ({
      bidsData: bids.slice(query.currentPage * 10,
        query.currentPage * 10 + query.number),
      totalPages: Math.floor(bids.length / 10),
    }));
}

