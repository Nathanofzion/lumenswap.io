function fetchAuctionWinners(query) {
  if (!query) {
    return new Promise((reslove) => setTimeout(reslove, 1000))
      .then(() => ({
        winnersData: winners,
      }));
  }
  if (query.currentPage === 1) {
    return new Promise((reslove) => setTimeout(reslove, 1000))
      .then(() => ({
        winnersData: winners.slice(0, query.number),
        totalPages: Math.floor(winners.length / 10),
      }));
  }
  return new Promise((reslove) => setTimeout(reslove, 1000))
    .then(() => ({
      winnersData: winners.slice(query.currentPage * 10,
        query.currentPage * 10 + query.number),
      totalPages: Math.floor(winners.length / 10),
    }));
}

